.IFNDEF LOG_DECIMAL

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

.MACRO _LOG_DECIMAL NUMBER // CAN ALSO PUT REGISTER
	MOV X0, \NUMBER
	BL LOG_DECIMAL
.ENDM

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

LOG_DECIMAL:// {W0} LOG_DECIMAL({X0});
	// RETURNS NUMBER BYTES WRITTEN IN {W0}

  // POPULATE BUFFER WITH ASCII REPRESENTION
  // PLACE NUM DIGITS IN W3

  _LOG_TEXT .BUFFER W3
	RET

.BUFFER:
.SPACE 32

.ENDIF
